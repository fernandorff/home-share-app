// Generates scripts/rename-db-to-english.followup.sql: the constraint/index/sequence renames
// that remain after rename-db-to-english.sql, by pairing old/new names via their DEFINITIONS
// in two throwaway pglite DBs (A = old schema + renames, B = fresh new schema). Verifies the
// combined script leaves A's catalog byte-identical to B's.
import { execSync } from "node:child_process";
import { readFileSync, writeFileSync } from "node:fs";
import { PGlite } from "@electric-sql/pglite";

const NEW_SCHEMA = "scripts/schema.english.prisma";

const q = async (db, sql) => (await db.query(sql)).rows;

const constraints = (db) =>
  q(db, `SELECT rel.relname AS tbl, con.conname AS name, pg_get_constraintdef(con.oid) AS def
         FROM pg_constraint con JOIN pg_class rel ON rel.oid = con.conrelid
         JOIN pg_namespace nsp ON nsp.oid = rel.relnamespace WHERE nsp.nspname = 'public'`);
const indexes = (db) =>
  q(db, `SELECT indexname AS name, indexdef AS def FROM pg_indexes WHERE schemaname = 'public'`);
const sequences = (db) =>
  q(db, `SELECT sequencename AS name FROM pg_sequences WHERE schemaname = 'public'`);

const normIdx = (def, name) => def.replace(`INDEX "${name}"`, 'INDEX ?').replace(`INDEX ${name}`, 'INDEX ?');

const oldDdl = execSync("npx prisma migrate diff --from-empty --to-schema prisma/schema.prisma --script", { encoding: "utf8" });
const newDdl = execSync(`npx prisma migrate diff --from-empty --to-schema "${NEW_SCHEMA}" --script`, { encoding: "utf8" });

const a = new PGlite(); await a.waitReady; await a.exec(oldDdl);
await a.exec(readFileSync("scripts/rename-db-to-english.sql", "utf8"));
const b = new PGlite(); await b.waitReady; await b.exec(newDdl);

const out = [
  "-- Follow-up to rename-db-to-english.sql: constraint/index/sequence renames so the DB",
  "-- catalog is byte-identical to what the @map-free schema generates (prisma db push = no-op).",
  "-- GENERATED by scripts/generate-followup.mjs — do not hand-edit.",
  "",
];

// 1. constraints (pkey/fkey) — renaming a pkey constraint also renames its backing index
const consA = await constraints(a), consB = await constraints(b);
const consAByKey = new Map(consA.map((c) => [`${c.tbl}|${c.def}`, c]));
for (const cb of consB) {
  const ca = consAByKey.get(`${cb.tbl}|${cb.def}`);
  if (ca && ca.name !== cb.name) {
    const stmt = `ALTER TABLE "${cb.tbl}" RENAME CONSTRAINT "${ca.name}" TO "${cb.name}";`;
    out.push(stmt);
    await a.exec(stmt);
  }
}

// 2. remaining indexes (uniques/plain) — compare with names stripped out of the definition
const idxA = await indexes(a), idxB = await indexes(b);
const idxAByKey = new Map(idxA.map((i) => [normIdx(i.def, i.name), i]));
out.push("");
for (const ib of idxB) {
  const ia = idxAByKey.get(normIdx(ib.def, ib.name));
  if (ia && ia.name !== ib.name) {
    const stmt = `ALTER INDEX "${ia.name}" RENAME TO "${ib.name}";`;
    out.push(stmt);
    await a.exec(stmt);
  }
}

// 3. sequences — exactly one per renamed table (…_id_seq), pair by sorted position of the rest
const seqA = new Set((await sequences(a)).map((s) => s.name));
const seqB = new Set((await sequences(b)).map((s) => s.name));
const seqOnlyA = [...seqA].filter((s) => !seqB.has(s)).sort();
const seqOnlyB = [...seqB].filter((s) => !seqA.has(s)).sort();
const TABLE_MAP = {
  membro_grupo: "GroupMember", categoria_casa: "Category", plataforma: "Platform",
  forma_pagamento_casa: "PaymentMethod", item_compra: "ShoppingItem", acerto: "Settlement",
  registro_auditoria: "AuditLog", revisao_entidade: "EntityRevision",
};
out.push("");
for (const oldSeq of seqOnlyA) {
  const oldTable = oldSeq.replace(/_id_seq$/, "");
  const newSeq = `${TABLE_MAP[oldTable]}_id_seq`;
  if (!seqOnlyB.includes(newSeq)) throw new Error(`no pair for sequence ${oldSeq}`);
  const stmt = `ALTER SEQUENCE "${oldSeq}" RENAME TO "${newSeq}";`;
  out.push(stmt);
  await a.exec(stmt);
}

writeFileSync("scripts/rename-db-to-english.followup.sql", out.join("\n") + "\n");
console.log(`followup.sql written (${out.filter((l) => l.startsWith("ALTER")).length} statements)`);

// 4. final verification: catalogs must now be identical
const CATALOG_SQL = `
  SELECT 'table' AS kind, tablename AS name, '' AS detail FROM pg_tables WHERE schemaname = 'public'
  UNION ALL SELECT 'column', table_name || '.' || column_name, data_type FROM information_schema.columns WHERE table_schema = 'public'
  UNION ALL SELECT 'index', indexname, indexdef FROM pg_indexes WHERE schemaname = 'public'
  UNION ALL SELECT 'constraint', rel.relname || '.' || con.conname, pg_get_constraintdef(con.oid)
    FROM pg_constraint con JOIN pg_class rel ON rel.oid = con.conrelid
    JOIN pg_namespace nsp ON nsp.oid = rel.relnamespace WHERE nsp.nspname = 'public'
  UNION ALL SELECT 'sequence', sequencename, '' FROM pg_sequences WHERE schemaname = 'public'`;
const ca = new Map((await q(a, CATALOG_SQL)).map((r) => [`${r.kind}|${r.name}`, r.detail]));
const cb2 = new Map((await q(b, CATALOG_SQL)).map((r) => [`${r.kind}|${r.name}`, r.detail]));
let diffs = 0;
for (const [k, v] of cb2) if (!ca.has(k) || ca.get(k) !== v) { console.log(`STILL DIFFERS: ${k}`); diffs++; }
for (const k of ca.keys()) if (!cb2.has(k)) { console.log(`STILL EXTRA: ${k}`); diffs++; }
console.log(diffs === 0 ? "✅ catalogs identical — combined script verified complete" : `❌ ${diffs} residual differences`);
await a.close(); await b.close();
